SELECT
	T.TEAM_ID, T.TEAM_NAME, P.PLAYER_NAME, S.GOAL_TIME
FROM
	SCORE S
INNER JOIN
	PLAYER P
ON
	S.PLAYER_ID = P.PLAYER_ID
INNER JOIN
	TEAM T
ON
	P.TEAM_ID = T.TEAM_ID
WHERE
	GOAL_TIME > 45
ORDER BY 
	T.TEAM_ID, S.GOAL_TIME;
