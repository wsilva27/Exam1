SELECT
	T3.TEAM_ID, T3.TEAM_NAME, P.PLAYER_NAME, S.GOAL_TIME
FROM
	MATCHES M
INNER JOIN
	TEAM T1
ON
	M.TEAM_ID_1 = T1.TEAM_ID
INNER JOIN
	TEAM T2
ON
	M.TEAM_ID_2 = T2.TEAM_ID
LEFT JOIN
	SCORE S
ON
	M.MATCH_ID = S.MATCH_ID
INNER JOIN
	PLAYER P
ON
	S.PLAYER_ID = P.PLAYER_ID
INNER JOIN 
	TEAM T3
ON
	T3.TEAM_ID = P.TEAM_ID
WHERE S.GOAL_TIME > 45
GROUP BY M.MATCH_ID, M.TEAM_ID_1, M.TEAM_ID_2;

