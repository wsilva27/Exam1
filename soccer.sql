CREATE DATABASE SOCCER;
USE SOCCER;

CREATE TABLE Season (
	SEASON_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	SEASON_NAME VARCHAR(40) NOT NULL UNIQUE KEY
)ENGINE=InnoDB;

CREATE TABLE Leagues (
   LEAGUE_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   LEAGUE_NAME VARCHAR(40) NOT NULL UNIQUE KEY,
   SEASON_ID INT NOT NULL,
   FOREIGN KEY (SEASON_ID) 
   					REFERENCES Season(SEASON_ID)
)ENGINE=InnoDB;

CREATE TABLE Field_home (
	FIELD_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   FIELD_NAME VARCHAR(40) NOT NULL UNIQUE KEY,
   FIELD_ADDRESS VARCHAR(40) NOT NULL UNIQUE KEY,
   FIELD_CITY VARCHAR(40) NOT NULL,
   FIELD_STATE VARCHAR (10) NOT NULL
)ENGINE=InnoDB;
  
CREATE TABLE Team (
	TEAM_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	TEAM_NAME VARCHAR(40) NOT NULL UNIQUE KEY,
   LEAGUE_ID INT NOT NULL,
   FIELD_ID INT NOT NULL UNIQUE KEY,
   FOREIGN KEY (LEAGUE_ID)
						REFERENCES Leagues(LEAGUE_ID),
	FOREIGN KEY (FIELD_ID)
						REFERENCES Field_home(FIELD_ID)
)ENGINE=InnoDB;

CREATE TABLE Player (
	PLAYER_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   PLAYER_NAME VARCHAR (40) NOT NULL,
   PHONE_NUMBER VARCHAR(12) UNIQUE KEY,
   PLAYER_DOB DATE NOT NULL,
   JERSEY_NUMBER INT (5) UNIQUE KEY,
   PLAYER_POSITION VARCHAR (40) NOT NULL,
   IS_PLAYER_CAPTAIN VARCHAR (10) NULL,
   TEAM_ID INT NOT NULL,
   FOREIGN KEY (TEAM_ID)
						REFERENCES Team(TEAM_ID)
)ENGINE= InnoDB;

-- MANY TO MANY
CREATE TABLE TEAM_PLAYER (
	PLAYER_ID INT,
	TEAM_ID INT,
   FOREIGN KEY (TEAM_ID)
						REFERENCES Team(TEAM_ID),
   FOREIGN KEY (PLAYER_ID)
						REFERENCES Player(PLAYER_ID)
)ENGINE=InnoDB;

-- END OF MANY TO MANY TABLE

CREATE TABLE Matches (
	MATCH_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   TEAM_ID_1 INT NOT NULL,
   TEAM_ID_2 INT NOT NULL,
   FIELD_ID INT NOT NULL,
	MATCH_DATE DATE NOT NULL,
	FOREIGN KEY (FIELD_ID)
						REFERENCES Field_home(FIELD_ID),
   FOREIGN KEY (TEAM_ID_1)
						REFERENCES Team(TEAM_ID),
	FOREIGN KEY (TEAM_ID_2)
						REFERENCES Team(TEAM_ID)
)ENGINE=InnoDB;

CREATE TABLE Score (
	SCORE_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	GOAL_TIME TIME NOT NULL,
   MATCH_ID INT NOT NULL,
   TEAM_ID_1 INT,
   TEAM_ID_2 INT,
   FOREIGN KEY (TEAM_ID_1)
						REFERENCES Team (TEAM_ID),
	FOREIGN KEY (TEAM_ID_2)
						REFERENCES Team (TEAM_ID),
	FOREIGN KEY (MATCH_ID)
						REFERENCES Matches (MATCH_ID)
)ENGINE = InnoDB;

